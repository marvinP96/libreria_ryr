    private void CrearModelo2(){
        try {
            modelo2 = (new DefaultTableModel(
                null, new String [] {
                "id_empleado","Nombres",
                "apellido","direccion","telefono"
                }){
                Class[] types = new Class [] {
                java.lang.String.class,
                java.lang.String.class,
                java.lang.String.class,
                java.lang.String.class,
                java.lang.String.class,
                
                };
                boolean[] canEdit = new boolean [] {
                false,false,false,false,false
                };
                @Override
                public Class getColumnClass(int columnIndex) {
                return types [columnIndex];
                }
                @Override
                public boolean isCellEditable(int rowIndex, int colIndex){
                return canEdit [colIndex];
                }
            });
            this.jTable1.setModel(modelo2);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e.toString()+"error2");
        }
    }
    
    private void LlenarTabla()
    {
        try{
            Object a[]=null;
            List<Empleado>ListaEmpleado;
            ListaEmpleado= control_empleado.findEmpleadoEntities();
            for (int i = 0; i < ListaEmpleado.size(); i++) {
                modelo2.addRow(a);
                modelo2.setValueAt(ListaEmpleado.get(i).getIdEmpleado(),i, 0);
                modelo2.setValueAt(ListaEmpleado.get(i).getNombreEmp(),i, 1);
                modelo2.setValueAt(ListaEmpleado.get(i).getApellidoEmp(),i, 2);
                modelo2.setValueAt(ListaEmpleado.get(i).getDireccionEmp(),i, 3);
                modelo2.setValueAt(ListaEmpleado.get(i).getTelefonoEmp(),i, 4);
                               
            }
            
        }catch (Exception e){
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }

